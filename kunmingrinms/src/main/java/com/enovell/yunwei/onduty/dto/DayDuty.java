package com.enovell.yunwei.onduty.dto;

/**日值班计划模型
 * @author roysong
 *
 */
public class DayDuty {

	private String id;
	/**
	 * 值班人员名
	 */
	private String dutyUsername;
	/**
	 * 值班人员部门
	 */
	private String dutyOrg;
	/**
	 * 值班日期 yyyy-MM-dd
	 */
	private String dutyDate;
	/**
	 * 值班人员类型
	 */
	private Integer dutyType;
	/**
	 * 备注
	 */
	private String remark;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDutyUsername() {
		return dutyUsername;
	}
	public void setDutyUsername(String dutyUsername) {
		this.dutyUsername = dutyUsername;
	}
	public String getDutyOrg() {
		return dutyOrg;
	}
	public void setDutyOrg(String dutyOrg) {
		this.dutyOrg = dutyOrg;
	}
	public String getDutyDate() {
		return dutyDate;
	}
	public void setDutyDate(String dutyDate) {
		this.dutyDate = dutyDate;
	}
	public Integer getDutyType() {
		return dutyType;
	}
	public void setDutyType(Integer dutyType) {
		this.dutyType = dutyType;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
