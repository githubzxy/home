public class ZipFileDTO implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7662056176716398553L;

	/**
	 * 文件夹名称（必填）
	 */
	private String name;

	/**
	 * 附件（必填）
	 */
	private AttachFile af;

	/**
	 * 包名称
	 */
	private String zipName;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public AttachFile getAf() {
		return af;
	}

	public void setAf(AttachFile af) {
		this.af = af;
	}

	public String getZipName() {
		return zipName;
	}

	public void setZipName(String zipName) {
		this.zipName = zipName;
	}

}
