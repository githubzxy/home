public class FileSystemDTO implements Serializable {
	
	
	private static final long serialVersionUID = -8990322231072440507L;
	
	/**
	 * 文件ID
	 */
	private String id;
	
	/**
	 * 文件名称
	 */
	private String fileName;
	
	/**
	 * 文件类型
	 */
	private Integer fileType;
	
	/**
	 * 文件试用范围
	 */
	private Integer fileSuitableRange;
	
	/**
	 * 所属部门
	 */
	private String organization;
	
	/**
	 * 有效时间
	 */
	private Integer fileValidTime;
	
	/**
	 * 是否细化
	 */
	private String isDetailing;
	/**
	 * 备注
	 */
	private String remark;
	
	/**
	 * 创建人
	 */
	private String createUser;
	
	/**
	 * 创建开始时间
	 */
	private Date createStartDate;
	
	/**
	 * 查询时的开始时间
	 */
	private Date startDate;
	
	/**
	 * 查询时的结束时间
	 */
	private Date endDate;
	
	/**
	 * 上传文件
	 */
	private String uploadFileArr;
	
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public Integer getFileType() {
		return fileType;
	}

	public void setFileType(Integer fileType) {
		this.fileType = fileType;
	}

	public Integer getFileSuitableRange() {
		return fileSuitableRange;
	}

	public void setFileSuitableRange(Integer fileSuitableRange) {
		this.fileSuitableRange = fileSuitableRange;
	}
	
	public String getOrganization() {
		return organization;
	}

	public void setOrganization(String organization) {
		this.organization = organization;
	}

	public Integer getFileValidTime() {
		return fileValidTime;
	}

	public void setFileValidTime(Integer fileValidTime) {
		this.fileValidTime = fileValidTime;
	}

	public String getIsDetailing() {
		return isDetailing;
	}

	public void setIsDetailing(String isDetailing) {
		this.isDetailing = isDetailing;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getCreateUser() {
		return createUser;
	}

	public void setCreateUser(String createUser) {
		this.createUser = createUser;
	}

	public Date getCreateStartDate() {
		return createStartDate;
	}
	
	public void setCreateStartDate(Date createStartDate) {
		this.createStartDate = createStartDate;
	}

	public Date getStartDate() {
		return startDate;
	}

	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

	public String getUploadFileArr() {
		return uploadFileArr;
	}

	public void setUploadFileArr(String uploadFileArr) {
		this.uploadFileArr = uploadFileArr;
	}
	
}
