package com.enovell.yunwei.deviceConfig.dto;

import java.io.Serializable;
import java.util.Date;

/**
 * 项目名称：kunmingrinms
 * 类名称：DeviceConfigDTO
 * 类描述:  应急日常设备配置显示DTO
 * 创建人：yangsy 
 * 创建时间：2016-12-22 
 */

public class DeviceConfigDTO implements Serializable {
	
	
	private static final long serialVersionUID = 2235319864527315345L;
	/**
	 * 单位
	 */
	private Integer unit;
	/**
	 * 设备类型（使用前端映射）
	 */
	private Integer deviceCategory;
	/**
	 * 设备名
	 */
	private String name;
	/**
	 * 创建时间
	 */
	private Date date;
	
	
	public Integer getUnit() {
		return unit;
	}
	public void setUnit(Integer unit) {
		this.unit = unit;
	}
	public Integer getDeviceCategory() {
		return deviceCategory;
	}
	public void setDeviceCategory(Integer deviceCategory) {
		this.deviceCategory = deviceCategory;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	
}
